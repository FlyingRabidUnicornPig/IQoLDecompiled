using System;
using HardShellStudios.InputManager;
using UnityEngine;

public class hardInput : MonoBehaviour
{
	public static void ResetBinding(string key)
		=> Singleton<hardManager>.Instance.resetKey(key);

	public static void ResetAllBindings()
		=> Singleton<hardManager>.Instance.resetSavedKeys();

	public static KeyCode GetKeyCode(string key, bool player1Ithink)
		=> Singleton<hardManager>.Instance.GetKeyCode(key, player1Ithink);

	public static void MouseVisible(bool visible)
		=> Singleton<hardManager>.Instance.MouseVisble(visible);

	public static bool GetKey(string key)
		=> return Singleton<hardManager>.Instance.GetKey(key);

	public static void MouseLock(bool lock)
		=> Singleton<hardManager>.Instance.MouseLock(lock);

	public static bool GetKeyDown(string key)
		=> Singleton<hardManager>.Instance.GetKeyDown(key);

	public static float GetAxis(string key, string key2, float timeIThink)
		=> Singleton<hardManager>.Instance.GetAxis(key, key2, timeIThink);

	public static void ForceRebind(string key, bool key2, KeyCode controllerKeyIThink)
		=> Singleton<hardManager>.Instance.HardStartRebind(key, key2, controllerKeyIThink);

	public static hardInput.controllerType GetControllerType()
	{
		// whoa wtf dnspy actually saved a switch case?
		switch (Singleton<hardManager>.Instance.controllerType)
		{
			case 1:
				return hardInput.controllerType.PS4;
			case 2:
				return hardInput.controllerType.XBOX1;
			case 3:
				return hardInput.controllerType.XBOX360;
			default:
				return hardInput.controllerType.PS3;
		}
	}

	public static int GetControllerTypeIndex()
		=> Singleton<hardManager>.Instance.controllerType;

	public static void HardStartRebind(string key1, bool key2, hardInputUI bindFrom)
		=> Singleton<hardManager>.Instance.HardStartRebind(key1, key2, bindFrom);

	public static void SetControllerType(hardInput.controllerType controller)
		=> Singleton<hardManager>.Instance.setControllerType(controller);

	public static float GetAxis(string key, float timeIThink)
		=> Singleton<hardManager>.Instance.GetAxis(key, timeIThink);

	public static string GetKeyName(string key, bool useSecondBind)
		=> Singleton<hardManager>.Instance.GetKeyName(key, useSecondBind);

	public enum controllerType
	{
		PS3,
		PS4,
		XBOX1,
		XBOX360
		// Where's my homeboy the virtual boy controller?
	}
}
